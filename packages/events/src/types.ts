/**
 * @gsd/events - Shared event types for Socket.IO communication
 *
 * Event naming follows prefix:action pattern per CONTEXT.md:
 * - agent:* - Agent lifecycle events
 * - checkpoint:* - Checkpoint interaction events
 * - connection:* - Health/connection events
 */

// Prefixed event names per CONTEXT.md
export const EVENTS = {
  AGENT_TOKEN: 'agent:token',
  AGENT_START: 'agent:start',
  AGENT_END: 'agent:end',
  AGENT_ERROR: 'agent:error',
  AGENT_PHASE: 'agent:phase',
  TOOL_START: 'agent:tool_start',
  TOOL_END: 'agent:tool_end',
  CHECKPOINT_REQUEST: 'checkpoint:request',
  CHECKPOINT_RESPONSE: 'checkpoint:response',
  CONNECTION_HEALTH: 'connection:health',
} as const;

export type EventName = (typeof EVENTS)[keyof typeof EVENTS];

// Event payloads

/**
 * Token streaming event - sent for each token generated by the agent
 */
export interface TokenEvent {
  agentId: string;
  token: string;
  sequence: number;
}

/**
 * Agent start event - sent when an agent begins executing a task
 */
export interface AgentStartEvent {
  agentId: string;
  planId: string;
  taskName: string;
}

/**
 * Agent end event - sent when an agent completes (success, error, or checkpoint)
 */
export interface AgentEndEvent {
  agentId: string;
  status: 'success' | 'error' | 'checkpoint';
  summary?: string;
}

/**
 * Agent error event - sent when an agent encounters an error
 */
export interface AgentErrorEvent {
  agentId: string;
  code: string;
  message: string;
  recovery?: string;
  stack?: string;
}

/**
 * Checkpoint request event - sent when agent needs user input
 */
export interface CheckpointRequestEvent {
  checkpointId: string;
  type: 'human-verify' | 'decision' | 'human-action';
  prompt: string;
  options?: CheckpointOption[];
  timeoutMs?: number;
}

export interface CheckpointOption {
  id: string;
  label: string;
}

/**
 * Checkpoint response event - sent when user responds to a checkpoint
 */
export interface CheckpointResponseEvent {
  checkpointId: string;
  response: string;
}

/**
 * Health metrics event - sent periodically with server state
 *
 * Includes connected client count, room membership, uptime, and memory usage.
 */
export interface HealthMetricsEvent {
  connectedClients: number;
  roomCounts: Record<string, number>;
  uptime: number;
  memoryUsage: number;
}

/**
 * Connection health event - sent periodically for app-level health monitoring
 * (client-side latency tracking)
 */
export interface ConnectionHealthEvent {
  timestamp: string;
  latencyMs: number;
}

/**
 * Tool start event - sent when a tool begins executing
 */
export interface ToolStartEvent {
  agentId: string;
  toolId: string;
  toolName: string;
  input: unknown;
  sequence: number;
}

/**
 * Tool end event - sent when a tool completes
 */
export interface ToolEndEvent {
  agentId: string;
  toolId: string;
  success: boolean;
  output: string;
  duration: number;
  sequence: number;
}

/**
 * Agent phase event - sent when agent phase changes
 * Per CONTEXT.md: streaming/tool_executing/awaiting_checkpoint
 */
export interface AgentPhaseEvent {
  agentId: string;
  phase: 'streaming' | 'tool_executing' | 'awaiting_checkpoint';
  sequence: number;
}

// Socket.IO typed events

/**
 * Events emitted from server to client
 */
export interface ServerToClientEvents {
  [EVENTS.AGENT_TOKEN]: (event: TokenEvent) => void;
  [EVENTS.AGENT_START]: (event: AgentStartEvent) => void;
  [EVENTS.AGENT_END]: (event: AgentEndEvent) => void;
  [EVENTS.AGENT_ERROR]: (event: AgentErrorEvent) => void;
  [EVENTS.AGENT_PHASE]: (event: AgentPhaseEvent) => void;
  [EVENTS.TOOL_START]: (event: ToolStartEvent) => void;
  [EVENTS.TOOL_END]: (event: ToolEndEvent) => void;
  [EVENTS.CHECKPOINT_REQUEST]: (event: CheckpointRequestEvent) => void;
  [EVENTS.CONNECTION_HEALTH]: (event: HealthMetricsEvent) => void;
}

/**
 * Events emitted from client to server
 */
export interface ClientToServerEvents {
  [EVENTS.CHECKPOINT_RESPONSE]: (event: CheckpointResponseEvent) => void;
  'agent:subscribe': (agentId: string) => void;
  'agent:unsubscribe': (agentId: string) => void;
}

/**
 * Inter-server events (for future scaling)
 */
export interface InterServerEvents {
  ping: () => void;
}

/**
 * Socket data stored per connection
 */
export interface SocketData {
  projectId?: string;
  subscribedAgents: Set<string>;
}
